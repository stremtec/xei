use ratatui::style::Color;

#[derive(Debug, Clone, Copy)]
pub struct Theme {
    pub name: &'static str,
    #[allow(dead_code)]
    pub bg: Color,
    pub fg: Color,
    pub keyword: Color,
    pub string: Color,
    pub comment: Color,
    pub number: Color,
    pub type_name: Color,
    pub line_no: Color,
    pub editor_bg: Color,
    pub status_bg: Color,
    pub status_fg: Color,
    pub border: Color,
    pub selection_bg: Color,
    pub search_bg: Color,
    pub mode_normal: Color,
    pub mode_insert: Color,
    pub mode_visual: Color,
    pub mode_xlc: Color,
    pub completion_bg: Color,
    pub completion_selected: Color,
    pub completion_border: Color,
    pub explorer_bg: Color,
    pub explorer_fg: Color,
    pub explorer_dir: Color,
    pub explorer_selected: Color,
    pub terminal_bg: Color,
    #[allow(dead_code)]
    pub terminal_fg: Color,
    #[allow(dead_code)]
    pub terminal_prompt: Color,
    pub xlc_bg: Color,
    pub xlc_fg: Color,
    pub xlc_prompt: Color,
    pub xlc_border: Color,
    pub cursor: Color,
}

static THEMES: &[Theme] = &[OCEAN, MONOKAI, NORD, SOLARIZED, GRUVBOX, EVERFOREST, SAKURA, NEWSPAPER];

pub fn all_themes() -> &'static [Theme] {
    THEMES
}

pub fn find(name: &str) -> Option<&'static Theme> {
    THEMES
        .iter()
        .find(|t| t.name.to_lowercase() == name.to_lowercase())
}

